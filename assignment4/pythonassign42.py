
#explain using isupper and islower with different string

str='ALL IS WELL'
print(str.isupper())
#output:true
#because all string are in uppercase

str='All is Well'
print(str.isupper())
#output:false
#because all string are not in uppercase

str='all is well'
print(str.islower())
#output:true
#because all string are in lowercase


str='ALL is Well'
print(str.islower())
#output:false
#because all string are not in lowercase






